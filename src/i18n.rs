use std::env;

/// Language selection for the application
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Lang {
    En,
    Fr,
    De,
    Es,
    It,
    Pt,
    Nl,
    Pl,
    Sv,
    Ru,
    Ja,
    Zh,
    Ko,
}

impl Lang {
    /// Detect language from LANG/LC_ALL environment variable.
    pub fn detect() -> Self {
        let lang = env::var("LANG")
            .or_else(|_| env::var("LC_ALL"))
            .unwrap_or_default()
            .to_lowercase();
        if lang.starts_with("fr") {
            Lang::Fr
        } else if lang.starts_with("de") {
            Lang::De
        } else if lang.starts_with("es") {
            Lang::Es
        } else if lang.starts_with("it") {
            Lang::It
        } else if lang.starts_with("pt") {
            Lang::Pt
        } else if lang.starts_with("nl") {
            Lang::Nl
        } else if lang.starts_with("pl") {
            Lang::Pl
        } else if lang.starts_with("sv") {
            Lang::Sv
        } else if lang.starts_with("ru") {
            Lang::Ru
        } else if lang.starts_with("ja") {
            Lang::Ja
        } else if lang.starts_with("zh") || lang.starts_with("cn") {
            Lang::Zh
        } else if lang.starts_with("ko") {
            Lang::Ko
        } else {
            Lang::En
        }
    }
}

