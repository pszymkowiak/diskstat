use std::env;

/// Language selection for the application
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Lang {
    En,
    Fr,
}

impl Lang {
    /// Detect language from LANG/LC_ALL environment variable.
    pub fn detect() -> Self {
        let lang = env::var("LANG")
            .or_else(|_| env::var("LC_ALL"))
            .unwrap_or_default()
            .to_lowercase();
        if lang.starts_with("fr") {
            Lang::Fr
        } else {
            Lang::En
        }
    }
}

